/**
 * Class for generating XML messages for elements of type 'ScheduleType'.
 */
class ScheduleNode extends ComplexNode
{
  //--------------------------------------------------------------------------------------------------------------------
  protected function ensureDependencies()
  {
    foreach ($this->myChildNodes as $node)
    {
      $node->ensureDependencies();
    }
  }

  //--------------------------------------------------------------------------------------------------------------------
}

